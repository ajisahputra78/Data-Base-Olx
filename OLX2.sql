CREATE TABLE USER_OLX
(
ID_USER CHAR(5) PRIMARY KEY NOT NULL,
NAMA VARCHAR (30) not null,
EMAIL VARCHAR (30) CHECK(EMAIL LIKE '%@%.%') UNIQUE ,
TELEPON VARCHAR(12),
FOTO_PROFIL VARBINARY(MAX)
)

CREATE TABLE KATEGORI 
(
ID_KATEGORI CHAR(5) PRIMARY KEY NOT NULL,
KATEGORI VARCHAR(30)
)

CREATE TABLE JENIS_KATEGORI 
(
ID_JENIS_KATEGORI CHAR(5) PRIMARY KEY NOT NULL,
SUBKATEGORI VARCHAR(30),
