)

CREATE TABLE IKLAN
(
ID_IKLAN CHAR(5) PRIMARY KEY NOT NULL,
ID_PENJUAL CHAR(5) CONSTRAINT FK_PENGIKLANAN_ID_USER FOREIGN KEY REFERENCES USER_OLX(ID_USER),
JUDUL_IKLAN VARCHAR (25), 
HARGA INT,
DESKRIPSI VARCHAR (200),
FOTO VARBINARY(MAX),
WILAYAH VARCHAR (50),
)

CREATE TABLE DETAIL_IKLAN
(
ID_DETAIL_IKLAN CHAR(5) PRIMARY KEY NOT NULL,
ID_IKLAN CHAR(5) CONSTRAINT FK_DETAIL_IKLAN_IKLAN FOREIGN KEY REFERENCES IKLAN(ID_IKLAN),
