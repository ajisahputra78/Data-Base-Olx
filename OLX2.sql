ID_RUMAH_TANGGA CHAR(5) CONSTRAINT FK_DETAIL_IKLAN_RUMAH_TANGGA FOREIGN KEY REFERENCES RUMAH_TANGGA(ID_RUMAH_TANGGA),
ID_KEPERLUAN_PRIBADI CHAR(5) CONSTRAINT FK_DETAIL_IKLAN_KEPERLUAN_PRIBADI FOREIGN KEY REFERENCES KEPERLUAN_PRIBADI(ID_KEPERLUAN_PRIBADI),
